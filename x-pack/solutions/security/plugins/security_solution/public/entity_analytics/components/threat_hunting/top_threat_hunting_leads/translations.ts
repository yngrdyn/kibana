export const getStalenessLabel = (staleness: string): string => {
  switch (staleness) {
    case 'fresh':
      return STALENESS_FRESH;
    case 'stale':
      return STALENESS_STALE;
    case 'expired':
      return STALENESS_EXPIRED;
    default:
      return staleness;
  }
};

