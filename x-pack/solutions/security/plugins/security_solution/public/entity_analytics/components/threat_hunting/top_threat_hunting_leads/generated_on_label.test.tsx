import React from 'react';
import { render as rtlRender, screen } from '@testing-library/react';
import { I18nProvider } from '@kbn/i18n-react';

import { useDateFormat, useTimeZone } from '../../../../common/lib/kibana';
import { GeneratedOnLabel } from './generated_on_label';

jest.mock('../../../../common/lib/kibana');
const mockUseDateFormat = useDateFormat as jest.Mock;
const mockUseTimeZone = useTimeZone as jest.Mock;

const render = (ui: React.ReactElement) => rtlRender(ui, { wrapper: I18nProvider });

describe('GeneratedOnLabel', () => {
  beforeEach(() => {
    mockUseDateFormat.mockReturnValue('MMM D, YYYY @ HH:mm:ss.SSS');
  });

  it('formats the timestamp using the configured UTC time zone preference', () => {
    mockUseTimeZone.mockReturnValue('UTC');

    render(<GeneratedOnLabel timestamp="2026-07-16T22:32:00.000Z" />);

    expect(screen.getByText('Generated by AI on Jul 16, 2026 @ 22:32:00.000')).toBeInTheDocument();
  });

  it('formats the timestamp in the configured local time zone rather than forcing UTC', () => {
    mockUseTimeZone.mockReturnValue('America/Los_Angeles');

    render(<GeneratedOnLabel timestamp="2026-07-16T22:32:00.000Z" />);

    expect(screen.getByText('Generated by AI on Jul 16, 2026 @ 15:32:00.000')).toBeInTheDocument();
  });
});
